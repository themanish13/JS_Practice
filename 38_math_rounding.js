//rounding in math in js
console.log(Math.round(4.5)); // returns 5
console.log(Math.round(4.4)); // returns 4
console.log(Math.ceil(4.1));  // returns 5
console.log(Math.floor(4.9)); // returns 4
console.log(Math.trunc(4.9)); // returns 4
console.log(Math.trunc(-4.9)); // returns -4
console.log(Math.sign(-4)); // returns -1
console.log(Math.sign(4));  // returns 1
console.log(Math.sign(0));  // returns 0